namespace ArithmeticLibrary
{
    public class ArithmeticCalculations
    {
        public decimal Add(int x, int y)
        {
            return x + y;
        }

        public decimal Subtract(int x, int y)
        {
            return x - y;
        }

        public decimal Multiply(int x, int y)
        {
            return x * y;
        }

        public decimal Divide(int x, int y)
        {
            return x / y;
        }
    }
}
